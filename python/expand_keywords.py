def expand_keywords(rules: Dict) -> Dict:
    """为规则中的每个职能扩展岗位关键词"""
    
    funcs = rules.get('functions', {}).get('data', [])
    
    for func in funcs:
        func_code = func['code']
        func_name = func['name']
        
        # 收集所有岗位的同义词
        expanded_keywords = set(func.get('keywords', []))
        positions = func.get('positions', [])
        
        for pos in positions:
            if pos in POSITION_SYNONYMS:
                expanded_keywords.update(POSITION_SYNONYMS[pos])
        
        # 如果没有预定义的positions，根据职能名称生成一些通用关键词
        if not positions:
            expanded_keywords.update(generate_generic_keywords(func_name))
        
        # 更新关键词列表
        func['keywords'] = sorted(list(expanded_keywords))
        
        # 添加扩展说明
        func['_keywords_expanded'] = True
        func['_keywords_count'] = len(func['keywords'])
    
    # 更新元数据
    total_keywords = sum(len(f.get('keywords', [])) for f in funcs)
    rules['_meta']['stats']['total_keywords'] = total_keywords
    rules['_meta']['stats']['keywords_per_function_avg'] = round(total_keywords / len(funcs), 1)
    
    return rules


def generate_generic_keywords(func_name: str) -> List[str]:
    """根据职能名称生成通用关键词"""
    keywords = []
    
    # 添加职能名本身的各种变体
    keywords.append(func_name)
    
    # 常见后缀
    suffixes = ['工程师', '专员', '主管', '经理', '总监', '助理', '顾问', '师']
    for suffix in suffixes:
        if not func_name.endswith(suffix):
            keywords.append(func_name.replace('/', '') + suffix)
    
    return keywords


